# Function to get top N content-based recommendations for a user
def get_content_based_recommendations(email_address, current_category, model, n=3):
    user_items = df[
        (df["email_address"] == email_address) & (df["category"] == current_category)
    ]["service_name"].tolist()

    user_tfidf_matrix = tfidf_matrix[df["service_name"].isin(user_items).values]

    if user_tfidf_matrix.shape[0] == 0:
        # If no subscriptions, return empty
        return []

    user_tfidf_vector = np.asarray(user_tfidf_matrix.mean(axis=0)).flatten()

    tfidf_matrix_array = np.asarray(tfidf_matrix.A)
    similarity_scores = cosine_similarity(
        user_tfidf_vector.reshape(1, -1), tfidf_matrix_array
    ).flatten()

    sorted_indices = similarity_scores.argsort()[::-1]

    # removin duplicates
    seen = set()
    unique_recommendations = [
        {
            "service_name": df.iloc[i]["service_name"],
            "Estimated Rating": similarity_scores[i] * 5,
        }
        for i in sorted_indices
        if not (
            df.iloc[i]["service_name"] in seen or seen.add(df.iloc[i]["service_name"])
        )
    ]

    top_recommendations = unique_recommendations[:n]
    # print('Top N content-based recommendations:', top_recommendations)
    return top_recommendations


# Function to compare pricing
def compare_pricing(sub_df, category, age, is_student):
    # print(sub_df.columns)

    # avoid settingwithcopywarnigg
    sub_pro = sub_df.copy()

    try:
        if is_student == 0:
            sub_pro = sub_pro.loc[
                (sub_pro["plan_name"] != "Student") & (sub_pro["category"] == category)
            ]
    except:
        sub_pro = sub_pro.loc[(sub_pro["category"] == category)]

    duration_mapping = {"Monthly": 1, "Quarterly": 3, "Half-yearly": 6, "Yearly": 12}
    sub_pro["Duration"] = sub_pro["payment_frequency"].map(duration_mapping)
    sub_pro["EquivalentMonthlyPrice"] = sub_pro["priceAED"] / sub_pro["Duration"]

    min_price_indices = sub_pro.groupby("service_name")[
        "EquivalentMonthlyPrice"
    ].idxmin()
    best_pricing = sub_pro.loc[
        min_price_indices,
        ["service_name", "plan_name", "EquivalentMonthlyPrice", "priceAED"],
    ].reset_index(drop=True)

    # print('compare pricing:',best_pricing.sort_values(by="EquivalentMonthlyPrice"))

    return best_pricing.sort_values(by="EquivalentMonthlyPrice")


# Function to find popular subscriptions in gender
def popular_in_gender(df, gender, category):
    new_df = df.loc[(df["gender"] == gender) & (df["category"] == category)]
    result_df = new_df.groupby("service_name").size().reset_index(name="Count")
    result_df = result_df.sort_values(by="Count", ascending=False)
    result_df["Count"] = (result_df["Count"] / (result_df["Count"].sum())) * 100
    result_df.rename(columns={"Count": "Score"}, inplace=True)
    # print('compare gender:',result_df)

    return result_df


# Function to find popular subscriptions in age group
def popular_in_ageGroup(df, ageGroup, category):
    new_df = df.loc[(df["Age Group"] == ageGroup) & (df["category"] == category)]
    result_df = new_df.groupby("service_name").size().reset_index(name="Count")
    result_df = result_df.sort_values(by="Count", ascending=False)
    result_df["Count"] = (result_df["Count"] / (result_df["Count"].sum())) * 100
    result_df.rename(columns={"Count": "Score"}, inplace=True)
    # print('compare age:',result_df)

    return result_df


