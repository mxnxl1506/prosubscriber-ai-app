# Get top N recommendations for a specific user from the same category
def get_top_n_recommendations_surprise(model, email_address, category, n=3):
    # Get all subscriptions in the specified category
    category_subscriptions = df[df["category"] == category]["service_name"].unique()

    # Get subscriptions the user has not rated in the specified category
    user_rated_subscriptions = df[
        (df["email_address"] == email_address) & (df["category"] == category)
    ]["service_name"]
    user_unrated_subscriptions = list(
        set(category_subscriptions) - set(user_rated_subscriptions)
    )

    # Make predictions for unrated subscriptions
    unrated_predictions = [
        model.predict(email_address, subscription)
        for subscription in user_unrated_subscriptions
    ]

    # Sort predictions by estimated rating in descending order
    unrated_predictions.sort(key=lambda x: x.est, reverse=True)

    # Extract top N recommendations
    top_n_recommendations = unrated_predictions[:n]
    print(top_n_recommendations)

    # Return the subscription names and estimated ratings
    # print('top N recommendations  surprise: ',[{'Subscription': prediction.iid, 'Estimated Rating': prediction.est} for prediction in top_n_recommendations])
    return [
        {"service_name": prediction.iid, "Estimated Rating": prediction.est}
        for prediction in top_n_recommendations
    ]


