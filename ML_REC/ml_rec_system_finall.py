# Function to recommend for a user segment
def recommend_for_user_segment(email_address, current_category, model, algo, n=3):
    collaborative_recommendations = get_top_n_recommendations_surprise(
        model, email_address, current_category, n
    )
    content_based_recommendations = get_content_based_recommendations(
        email_address, current_category, algo, n
    )
    # print(df)
    user_data = df[
        (df["email_address"] == email_address) & (df["category"] == current_category)
    ].iloc[0]
    # print(user_data)

    pricing_recommendations = compare_pricing(
        df, current_category, user_data["Age"], user_data["is_student"]
    )

    gender_recommendations = popular_in_gender(
        df, user_data["gender"], current_category
    )

    age_group_recommendations = popular_in_ageGroup(
        df, user_data["Age Group"], current_category
    )

    pricing_plan_name = user_data.get("plan_name", None)

    # filtering the dataframe
    collaborative_recommendations = [
        rec
        for rec in collaborative_recommendations
        if "plan_name" not in rec
        or ("plan_name" in rec and rec["plan_name"] == pricing_plan_name)
    ]
    # print("collaborative_recommendations:",collaborative_recommendations)

    content_based_recommendations = [
        rec
        for rec in content_based_recommendations
        if "plan_name" not in rec
        or ("plan_name" in rec and rec["plan_name"] == pricing_plan_name)
    ]
    # print("content_based_recommendations:", content_based_recommendations)

    # print("pricing_recommendations:\n", pricing_recommendations)
    # print("gender_recommendations:\n", gender_recommendations)
    # print("age_group_recommendations:\n", age_group_recommendations)

    # Combining recommendations
    collaborative_df = pd.DataFrame(collaborative_recommendations)
    content_based_df = pd.DataFrame(content_based_recommendations)
    hybrid_df = collaborative_df.merge(
        content_based_df,
        on="service_name",
        how="outer",
        suffixes=("_collaborative", "_content_based"),
    )
    hybrid_df = hybrid_df.merge(
        pricing_recommendations,
        on="service_name",
        how="outer",
        suffixes=("", "_pricing"),
    )
    hybrid_df = hybrid_df.merge(
        gender_recommendations, on="service_name", how="outer", suffixes=("", "_gender")
    )
    hybrid_df = hybrid_df.merge(
        age_group_recommendations,
        on="service_name",
        how="outer",
        suffixes=("", "_age_group"),
    )

    # Convert the list of dictionaries to a DataFrame
    # hybrid_df = pd.DataFrame(hybrid_recommendations)

    # each row, find its score in pricing,gender,age, and then add onto the hybrid rec df a column stating advantages.
    # for example, if x is the current subscription, and y has a cheaper price, we add cheaper in the advantages column for row of y
    current_subscription_price = user_data.get("Price(AED)", 0)
    current_subscription = user_data.get("service_name")
    hybrid_df["advantages"] = "Similar"

    # removing repeated recommendations
    hybrid_df = hybrid_df.drop_duplicates(subset="service_name", keep="first")

    for index, row in hybrid_df.iterrows():
        # checking if its cheaper
        # print(row['EquivalentMonthlyPrice'], current_subscription_price)

        if (
            "EquivalentMonthlyPrice" in row
            and row["EquivalentMonthlyPrice"] < current_subscription_price
        ):
            hybrid_df.at[index, "advantages"] += ", Cheaper ({:.2f} AED/month)".format(
                row["EquivalentMonthlyPrice"]
            )

        if "service_name" in row and row["service_name"] in [
            "Youtube Music",
            "Spotify",
        ]:
            hybrid_df.at[index, "advantages"] += ", Offers Student plan_names"

        # checking the popularity
        try:
            current_sub_score_age = hybrid_df.loc[
                hybrid_df["service_name"] == current_subscription
            ]["Score_age_group"].unique()[0]
            current_sub_score_gender = hybrid_df.loc[
                hybrid_df["service_name"] == current_subscription
            ]["Score"].unique()[0]
            if (
                row["Score"] > current_sub_score_age
                or row["Score"] > current_sub_score_gender
            ):
                hybrid_df.at[index, "advantages"] += ", More popular in demographic"
        except:
            pass

    hybrid_df = hybrid_df[["service_name", "advantages"]]
    hybrid_df["email_address"] = email_address
    hybrid_df["category"] = current_category

    return hybrid_df.head(n)


